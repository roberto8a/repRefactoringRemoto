package miCalculadora;

public class Calculadora {
	public double suma(double x,double y)
	{
		return x+y;
	}
	public double resta(double x,double y)
	{
		return x-y;
	}
	public double mul(double x, double y)
	{
		return x*y;
	}
	public double div(double x, double y)
	{
		return x/y;
	}
}
